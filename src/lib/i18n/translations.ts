// Helper type to convert literal types to string while preserving readonly
type DeepStringify<T> = T extends string
  ? string
  : T extends readonly string[]
  ? readonly string[]
  : T extends object
  ? { readonly [K in keyof T]: DeepStringify<T[K]> }
  : T;

